let warna = "kuning";
// Tulis kode switch statement di sini

switch (warna){
    case "merah" :
        console.log ("lampu merah : berhenti");
        break;
        case "kuning" :
            console.log ("lampu kuning : hati-hati, bersiap berhenti");
            break;
            case "hijau":
                console.log ("lampu hijau : jalan");
                break;
                default : 
                console.log ("lampu tidak berfungsi");

}